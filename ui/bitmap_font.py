class BitmapFont:
    def __init__(self, *, scale: int = 2) -> None:
        self.scale = max(1, scale)

    def render(self, text: str, _antialias: bool, color: tuple[int, int, int]) -> pygame.Surface:
        message = text.upper()
        glyph_width = 5 * self.scale
        glyph_height = 7 * self.scale
        spacing = self.scale

        width = max(1, len(message) * (glyph_width + spacing))
        surface = pygame.Surface((width, glyph_height), pygame.SRCALPHA)

        x_offset = 0
        for char in message:
            glyph = GLYPHS.get(char, GLYPHS["?"])
            self._draw_glyph(surface, glyph, x_offset, color)
            x_offset += glyph_width + spacing

        return surface

    def _draw_glyph(
        self,
        surface: pygame.Surface,
        glyph: tuple[str, ...],
        x_offset: int,
        color: tuple[int, int, int],
    ) -> None:
        for y, row in enumerate(glyph):
            for x, pixel in enumerate(row):
                if pixel != "1":
                    continue
                rect = pygame.Rect(
                    x_offset + x * self.scale,
                    y * self.scale,
                    self.scale,
                    self.scale,
                )
                pygame.draw.rect(surface, color, rect)
